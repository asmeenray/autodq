/*Create database*/
CREATE DATABASE star_wars;
\connect star_wars;


/*Create tables*/
CREATE TABLE planet(
    id INTEGER PRIMARY KEY,
    name TEXT,
    rotation_period INTEGER,
    orbital_period INTEGER,
    diameter INTEGER,
    climate TEXT,
    gravity TEXT,
    terrain TEXT,
    surface_water TEXT,
    population BIGINT,
    created_date TIMESTAMP,
    updated_date TIMESTAMP,
    url TEXT
);

CREATE TABLE people(
    id INTEGER PRIMARY KEY,
    name TEXT,
    height INTEGER,
    mass FLOAT,
    hair_color TEXT,
    skin_color TEXT,
    eye_color TEXT,
    birth_year TEXT,
    gender TEXT,
    planet_id INTEGER NOT NULL REFERENCES planet(id),
    created_date TIMESTAMP,
    updated_date TIMESTAMP,
    url TEXT
);

CREATE TABLE daily_nb_droids_built(
    id SERIAL PRIMARY KEY,
    built_date DATE,
    nb_droids INTEGER
);

CREATE TABLE daily_nb_droids_destroyed(
    id SERIAL PRIMARY KEY,
    destroyed_date DATE,
    nb_droids INTEGER
);

CREATE TABLE daily_nb_droids_repaired(
    id SERIAL PRIMARY KEY,
    repaired_date DATE,
    nb_droids INTEGER
);


